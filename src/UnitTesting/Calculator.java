package UnitTesting;

public class Calculator {

    public int addition(int a, int b)
    {
        int total = a+b;
        return total;
    }

    public int subtraction(int a, int b)
    {
        int total = a-b;
        return total;
    }
    public double multiply(double a, double b)
    {
        double total = a+b;
        return total;
    }
}
